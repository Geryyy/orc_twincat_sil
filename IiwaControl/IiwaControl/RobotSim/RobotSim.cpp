///<AutoGeneratedContent id="ImplementationOf_ITcCyclic">
HRESULT CRobotSim::CycleUpdate(ITcTask* ipTask, ITcUnknown* ipCaller, ULONG_PTR context)
{
	HRESULT hr = S_OK;
	
	// torque input
	JointVector tau(m_Inputs.torque.Values);

	// RNEA - forward dynamics computation
	mju_copy(mj_data->ctrl, tau.data(), mj_model->nu);   // if using actuators
	// OR directly:
	//mju_copy(d->qfrc_applied, my_torques, m->nv);  // applied generalized forces

	mj_forward(mj_model, mj_data);

	// Result: accelerations in d->qacc  (length m->nv)
	JointVector qdd = Eigen::Map<JointVector>(mj_data->qacc);

	// integration
	qd += Ts.toSec() * qdd;
	q += Ts.toSec() * qd;

	// update mj data
	mju_copy(mj_data->qvel, qd.data(), mj_model->nv); 
	mju_copy(mj_data->qpos, q.data(), mj_model->nq);  

	// return joint state
	JointVector::Map(m_Outputs.joint_state.Values) = q;
	JointVector::Map(m_Outputs.torque_motor.Values) = JointVector::Zero();
	JointVector::Map(m_Outputs.torque_sensor.Values) = JointVector::Zero();


	return hr;
}
