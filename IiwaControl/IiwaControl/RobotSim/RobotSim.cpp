///////////////////////////////////////////////////////////////////////////////
HRESULT CRobotSim::AddModuleToCaller()
{
	m_Trace.Log(tlVerbose, FENTERA);

	HRESULT hr = S_OK;
	if ( m_spCyclicCaller.HasOID() )
	{
		if ( SUCCEEDED_DBG(hr = m_spSrv->TcQuerySmartObjectInterface(m_spCyclicCaller)) )
		{
			if ( FAILED(hr = m_spCyclicCaller->AddModule(m_spCyclicCaller, THIS_CAST(ITcCyclic))) )
			{
				m_spCyclicCaller = NULL;
			}
		}
	}
	else
	{
		hr = ADS_E_INVALIDOBJID; 
		SUCCEEDED_DBGT(hr, "Invalid OID specified for caller task");
	}

	m_Trace.Log(tlVerbose, FLEAVEA "hr=0x%08x", hr);
	return hr;
}

///////////////////////////////////////////////////////////////////////////////
VOID CRobotSim::RemoveModuleFromCaller()
{
	m_Trace.Log(tlVerbose, FENTERA);

	if ( m_spCyclicCaller )
	{
		m_spCyclicCaller->RemoveModule(m_spCyclicCaller);
	}
	m_spCyclicCaller	= NULL;

	m_Trace.Log(tlVerbose, FLEAVEA);
}

// Reads the binary file and stores its contents into CArcController::model_binary
HRESULT CRobotSim::readBinaryModel(PCCH fileName)
{
	HRESULT hr = S_OK;
	m_Trace.Log(tlVerbose, FENTERA);

	// get file size to allocate space for model_binary variable in which model will be saved
	TcFileStatus FileStatus = { 0 };
	hr = m_spFileAccess->FileGetStatus(fileName, &FileStatus);
	ULONGLONG cbData = 0;
	if (SUCCEEDED(hr)) {
		m_Trace.Log(tlVerbose, FENTERA "Model size: %i", FileStatus.ulFileSize);
		if (FileStatus.ulFileSize >= UINT_MAX) {
			m_Trace.Log(tlVerbose, FENTERA "MJB file size too large!");
			return E_ABORT;
		}
		model_size = static_cast<UINT>(FileStatus.ulFileSize);
		// model_binary is allocated on the heap as its size is a priori not known, and deleted in SetObjStateOS()
		model_binary = new UCHAR[model_size];
	}
	else {
		m_Trace.Log(tlAlways, FLEAVEA "Error while reading file!");
		return hr;
	}

	// open file
	TcFileHandle hFile = NULL;
	hr = m_spFileAccess->FileOpen(fileName, static_cast<TcFileAccessMode>(amBinary | amRead), &hFile);

	if (SUCCEEDED(hr)) {
		// read file
		UINT pcbRead = 0;
		hr = m_spFileAccess->FileRead(hFile, model_binary, model_size, &pcbRead);
	}

	// close file
	hr = m_spFileAccess->FileClose(hFile);
	m_Trace.Log(tlVerbose, FLEAVEA "hr=0x%08x", hr);
	return hr;
}
