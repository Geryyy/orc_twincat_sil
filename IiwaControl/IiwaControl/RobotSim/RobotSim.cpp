// State transition from PREOP to SAFEOP
//
// Initialize input parameters 
// Allocate memory
HRESULT CRobotSim::SetObjStatePS(PTComInitDataHdr pInitData)
{
	m_Trace.Log(tlVerbose, FENTERA);
	HRESULT hr = S_OK;
	IMPLEMENT_ITCOMOBJECT_EVALUATE_INITDATA(pInitData);

	// TODO: Add initialization code

	// ------------------- READ IN MUJOCO FILE -------------	
	// Create file access instance
	hr = m_spSrv->TcCreateInstance(CID_TcFileAccess, IID_ITcFileAccess, (PPVOID)(&m_spFileAccess));

	// Choose mjb file as per TwinCAT parameters
	std::string model_path = "C:\\TwinCAT\\3.1\\Boot\\";
	if (m_MujocoModel == RobotSimMujocoModel::iiwa_hanging)
		model_path.append("iiwa_hanging.mjb");
	else if (m_MujocoModel == RobotSimMujocoModel::iiwa_hanging_with_mass_gripped)
		model_path.append("iiwa_hanging_with_mass_gripped.mjb");
	else if (m_MujocoModel == RobotSimMujocoModel::custom)
		model_path.append(m_CustomMujocoModelPath);
	else
		return E_FAIL;
	strcpy(m_Outputs.model_path, model_path.c_str());

	if (SUCCEEDED(hr)) {
		ITComObjectPtr spFileAccessObj = m_spFileAccess;
		hr = spFileAccessObj->TcSetObjState(TCOM_STATE_PREOP, m_spSrv, pInitData);
		if (SUCCEEDED(hr)) {
			// read Mujoco binary file (iiwa.mjb) from _Boot directory
			m_Trace.Log(tlVerbose, FENTERA "Reading MJB file!");
			// %BOOTPRJPATH% und %TC_TARGETPATH% funktionieren hier nicht, wieso?
			hr = readBinaryModel(model_path.c_str());
		}
	}

	mj_model = mj_loadModelBuffer((void*)model_binary, (unsigned int)model_size, m_Trace);
	mj_data = mj_makeData(mj_model);
	// ------------------- MUJOCO END ----------------------

	orc::log::start_logging_twincat(m_TraceLevelMax, &m_Trace);
	m_Trace.Log(tlAlways, FLEAVEA "ORC initialized!", hr);
	m_Trace.Log(tlVerbose, FLEAVEA "hr=0x%08x", hr);

	return hr;
}

///////////////////////////////////////////////////////////////////////////////
// State transition from SAFEOP to OP
//
// Register with other TwinCAT objects
HRESULT CRobotSim::SetObjStateSO()
{
	m_Trace.Log(tlVerbose, FENTERA);
	HRESULT hr = S_OK;

	// TODO: Add any additional initialization
	t = 0.0;
	q = JointVector::Zero();
	qd = JointVector::Zero();

	// read cycle time from TcCom Module Parameter
	Ts = m_Ts;
	m_Trace.Log(tlAlways, FLEAVEA "Cycle Time Ts = %f", Ts);

	// If following call is successful the CycleUpdate method will be called, 
	// possibly even before method has been left.
	hr = FAILED(hr) ? hr : AddModuleToCaller(); 

	// Cleanup if transition failed at some stage
	if ( FAILED(hr) )
	{
		RemoveModuleFromCaller(); 
	}

	m_Trace.Log(tlVerbose, FLEAVEA "hr=0x%08x", hr);
	return hr;
}

