HRESULT CUdpInterface::ReceiveData(ULONG ipAddr, USHORT udpDestPort, USHORT udpSrcPort, ULONG nData, PVOID pData, ETYPE_VLAN_HEADER* pVlan)
{
	HRESULT hr = S_OK;
	
	//m_Trace.Log(tlInfo, FLEAVEA "udpSrcPort: %d udpDstPort: %d \n", udpSrcPort, udpDestPort);

	switch (udpDestPort) {

	/* Iiwa */

	case orc::robots::Iiwa::SERVER_PORT:
		//m_Iiwa_Outputs.rx_msg.request = true;
		//m_Iiwa_Outputs.rx_msg.size = sizeof(m_Iiwa_Outputs.rx_msg.data);
		//memcpy(&(m_Iiwa_Outputs.rx_msg.data[0]), pData, sizeof(m_Iiwa_Outputs.rx_msg.data));

		m_Trace.Log(tlInfo, FLEAVEA "ARC Trajectory received: IP: %d.%d.%d.%d udpSrcPort: %d DataSize: %d (hr2=%x) \n", ((PBYTE)&ipAddr)[3], ((PBYTE)&ipAddr)[2], ((PBYTE)&ipAddr)[1], ((PBYTE)&ipAddr)[0], udpSrcPort, nData, hr);

		// move received data into iiwa_msg_buffer
		if(iiwa_buf_write_index < (MSG_BUFFER_LEN-1)){
			if (nData <= sizeof(m_Iiwa_Outputs.rx_msg.data)) {
				iiwa_buf_write_index++;
				if (iiwa_buf_read_index < 0) iiwa_buf_read_index = 0;
				iiwa_msg_buffer[iiwa_buf_write_index].size = static_cast<USHORT>(nData);
				memcpy(&(iiwa_msg_buffer[iiwa_buf_write_index].data), pData, nData);
			}
			else {
				m_Trace.Log(tlInfo, FLEAVEA "ARC Trajectory received: Too much data received! Msg ignored! \n");
			}
		}
		else {
			m_Trace.Log(tlInfo, FLEAVEA "ARC Trajectory received: Msg Buffer full! Msg ignored! \n");
		}
		
		break;

	case orc::robots::Iiwa::SIL_CONTROLLER_PORT:
		// SIL receive actual joint state
		if (nData >= sizeof(m_Iiwa_Sil_Output.joint_state)) {
			memcpy(&(m_Iiwa_Sil_Output.joint_state), pData, sizeof(m_Iiwa_Sil_Output.joint_state));
		}
		break;

	/* Linear Axis */
	case orc::robots::LinearAxis::SERVER_PORT:
		m_Trace.Log(tlInfo, FLEAVEA "Linear Axis Trajectory received: IP: %d.%d.%d.%d udpSrcPort: %d DataSize: %d (hr2=%x) \n", ((PBYTE)&ipAddr)[3], ((PBYTE)&ipAddr)[2], ((PBYTE)&ipAddr)[1], ((PBYTE)&ipAddr)[0], udpSrcPort, nData, hr);

		// move received data into linax_msg_buffer
		if (linax_buf_write_index < (MSG_BUFFER_LEN - 1)) {
			if (nData <= sizeof(m_LinAxis_Outputs.rx_msg.data)) {
				linax_buf_write_index++;
				if (linax_buf_read_index < 0) linax_buf_read_index = 0;
				linax_msg_buffer[linax_buf_write_index].size = static_cast<USHORT>(nData);
				memcpy(&(linax_msg_buffer[linax_buf_write_index].data), pData, nData);
			}
			else {
				m_Trace.Log(tlInfo, FLEAVEA "Linear Axis Trajectory received: Too much data received! Msg ignored! \n");
			}
		}
		else {
			m_Trace.Log(tlInfo, FLEAVEA "Linear Axis Trajectory received: Msg Buffer full! Msg ignored! \n");
		}

		break;

	case orc::robots::LinearAxis::SIL_CONTROLLER_PORT:
		// SIL receive actual joint state
		if (nData >= sizeof(m_LinAxis_Sil_Output.joint_state)) {
			memcpy(&(m_LinAxis_Sil_Output.joint_state), pData, sizeof(m_LinAxis_Sil_Output.joint_state));
		}
		break;

	default:
		m_Trace.Log(tlInfo, FLEAVEA "UDP ReceiveData: IP: %d.%d.%d.%d udpSrcPort: %d DataSize: %d (hr2=%x) \n", ((PBYTE)&ipAddr)[3], ((PBYTE)&ipAddr)[2], ((PBYTE)&ipAddr)[1], ((PBYTE)&ipAddr)[0], udpSrcPort, nData, hr);
	}

	return hr;
}
