///<AutoGeneratedContent id="ImplementationOf_ITcCyclic">
HRESULT COrcLinAxisContr::CycleUpdate(ITcTask* ipTask, ITcUnknown* ipCaller, ULONG_PTR context)
{
	HRESULT hr = S_OK;

	/* Timing */
	hr = FAILED(hr) ? hr : ipTask->GetCycleTime(&TaskCycleTime);
	Ts = ((double)TaskCycleTime) / ((double)1e9);

	// current time
	t += Ts;

	/* UDP input*/

	if (m_spudp_rx_msg != NULL) {
		if (m_spudp_rx_msg->request) {
			m_spudp_rx_msg->request = false;
			orc::com::handle_receive(*orc_contr_ptr, &(m_spudp_rx_msg->data[0]), m_spudp_rx_msg->size);
		}
	}

	/* Control runs */
	/* Robot Control */

	// read inputs
	JointVector q_act(m_motor_interface_in.position_act.Values);
	JointVector q_dot_act(m_motor_interface_in.velocity_act.Values);
	JointVector tau_motor_act(m_motor_interface_in.force_act.Values);
	
	state_machine_ptr->run(m_motor_interface_in.motion_enabled,
		m_Inputs.auto_enable,
		m_Inputs.enable,
		m_Inputs.disable);

	// run controller
	JointVector vel_set;
	if (state_machine_ptr->is_enable_transition()) {
		orc::Time T_traj = 1.0;
		JointVector q_end = q_act;
		orc_contr_ptr->start(t, q_act, q_end, T_traj);
		m_Trace.Log(tlInfo, FLEAVEA "orc_contr->start() executed!", hr);
	}
	if (state_machine_ptr->get_state() == orc::logic::RobotStatus::ENABLE) {
		// select desired torque based on state
		orc_contr_ptr->set_q_act(q_act);
		if (orc_contr_ptr->update(t))
			vel_set = orc_contr_ptr->get_tau_act();
		else
			return EXIT_FAILURE;
	}
	else {
		vel_set = JointVector::Zero();
	}
	
	// robot interface outputs
	// force setpoint is unused
	JointVector::Map(m_motor_interface_out.force_set.Values) = JointVector::Zero();
	JointVector::Map(m_motor_interface_out.velocity_set.Values) = vel_set;
	JointVector::Map(m_motor_interface_out.position_set.Values) = orc_contr_ptr->get_q_set();
	m_motor_interface_out.mode_of_operation = ModeOfOperation_e::Velocity;
	m_motor_interface_out.motion_enable = 0.0;
	m_motor_interface_out.motion_disable = 0.0;
	if (state_machine_ptr->is_disable_transition()) {
		m_motor_interface_out.motion_disable = 1.0;
	}
	else if (state_machine_ptr->is_enable_transition()) {
		m_motor_interface_out.motion_enable = 1.0;
	}
	// robot interface works with double: convert bool to double
	double motor_enable = m_Inputs.auto_enable ? 1.0 : 0.0;
	m_motor_interface_out.motor_enable.Values[0] = motor_enable;
	m_motor_interface_out.motor_enable.Values[1] = motor_enable;

	/* UDP output */
	if (m_spudp_tx_msg != NULL) {
		m_spudp_tx_msg->request = true;
		auto msg_size = orc::com::send_robot_data(*orc_contr_ptr, t, state_machine_ptr->get_state(), m_spudp_tx_msg->data, sizeof(m_spudp_tx_msg->data));
		m_spudp_tx_msg->size = msg_size;
	}

	/* set outputs */

	// actual values
	JointVector::Map(m_Outputs.q_act.Values) = orc_contr_ptr->get_q_act();
	JointVector::Map(m_Outputs.q_dot_act.Values) = orc_contr_ptr->get_q_dot_act();

	// Jointspace setpoints
	JointVector::Map(m_Outputs.q_set.Values) = orc_contr_ptr->get_q_set();
	JointVector::Map(m_Outputs.q_dot_set.Values) = orc_contr_ptr->get_q_dot_set();
	JointVector::Map(m_Outputs.q_dotdot_set.Values) = orc_contr_ptr->get_q_dotdot_set();

	// control error
	JointVector::Map(m_Outputs.e_jointspace.Values) = orc_contr_ptr->get_joint_error();

	// torque setpoint
	JointVector::Map(m_Outputs.tau_set.Values) = orc_contr_ptr->get_tau_act();

	return hr;
}
